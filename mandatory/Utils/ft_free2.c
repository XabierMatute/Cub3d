#include "../cub3D.h"

void	ft_free2(void **arr)
{
	int	i;

	i = -1;
	if (arr)
	{
		while (arr[++i])
		{
			free(arr[i]);
			arr[i] = NULL;
		}
		free(arr);
	}
	arr = NULL;
}