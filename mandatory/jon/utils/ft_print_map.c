# include "../cub3d.h"

void	ft_print_map(char **map)
{
	int	i;
	int	j;

	printf("MAPA: \n");
	i = -1;
	while (map[++i])
	{
		j = -1;
		while (map[i][++j])
			printf("%c", map[i][j]);
		printf("\n");
	}
}
